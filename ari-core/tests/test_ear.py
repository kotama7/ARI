_TRANSFORM_SRC = (
    Path(__file__).parent.parent.parent / "ari-skill-transform" / "src"
).resolve()
if str(_TRANSFORM_SRC) not in sys.path:
    sys.path.insert(0, str(_TRANSFORM_SRC))


# ── helpers ─────────────────────────────────────────────────────────────


def _get_generate_ear():
    """Import generate_ear from ari-skill-transform/src/server.py."""
    import importlib

    ts = str(_TRANSFORM_SRC)
    if ts in sys.path:
        sys.path.remove(ts)
    sys.path.insert(0, ts)

    if "server" in sys.modules:
        del sys.modules["server"]
    module = importlib.import_module("server")
    fn = module.generate_ear
    if hasattr(fn, "fn"):
        fn = fn.fn
    elif hasattr(fn, "__wrapped__"):
        fn = fn.__wrapped__
    return fn


def _sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _make_chain_checkpoint(tmp_path: Path, *, with_uploads: bool = False,
                           with_figures: bool = False,
                           with_publish_license: str | None = None) -> Path:
    """Build a 3-node draft → improve → validation checkpoint with reports.

    Each node has a real work_dir under experiments/<run_id>/<node_id>/, and a
    node_report.json describing its contribution. The validation node is best
    (highest scientific_score) but contributes no new files — it must still
    appear in the chain via always_include.
    """
    workspace = tmp_path / "ws"
    run_id = "exp_run"
    ckpt = workspace / "checkpoints" / run_id
    ckpt.mkdir(parents=True)
    exp_root = workspace / "experiments" / run_id

    # Set up work_dirs.
    nodes_def = [
        ("node_draft",      None,           0, "draft",
         {"throughput": 100.0, "_scientific_score": 0.45},
         {"main.cpp": "int main(){return 0;}\n",
          "run_job.sh": "#!/bin/bash\ng++ -O2 main.cpp -o main\n./main --out result.csv\n"},
         "Initial naïve baseline.",
         {"added": ["main.cpp", "run_job.sh"], "modified": []}),
        ("node_improve",    "node_draft",   1, "improve",
         {"throughput": 175.5, "_scientific_score": 0.82},
         {"main.cpp": "int main(){/* tiled */ return 0;}\n",  # modified
          "run_job.sh": "#!/bin/bash\ng++ -O3 main.cpp -o main\n./main --out result.csv\n",  # modified
          "tiling.h": "#define TILE 32\n"},  # added
         "Introduced loop tiling (TILE=32).",
         {"added": ["tiling.h"], "modified": ["main.cpp", "run_job.sh"]}),
        ("node_validation", "node_improve", 2, "validation",
         {"throughput": 175.0, "_scientific_score": 0.91},
         {"main.cpp": "int main(){/* tiled */ return 0;}\n",  # inherited
          "run_job.sh": "#!/bin/bash\ng++ -O3 main.cpp -o main\n./main --out result.csv\n",  # inherited
          "tiling.h": "#define TILE 32\n"},  # inherited
         "Re-ran with 5 seeds; variance under 2%.",
         {"added": [], "modified": []}),
    ]

    nodes_dict: list[dict] = []
    for nid, parent, depth, label, metrics, files, delta, fc in nodes_def:
        wd = exp_root / nid
        wd.mkdir(parents=True)
        for fn, body in files.items():
            (wd / fn).write_text(body)
        # Build the node_report.json.
        report = {
            "schema_version": 1,
            "node_id": nid,
            "parent_id": parent,
            "ancestor_ids": [],
            "label": label,
            "raw_label": "",
            "depth": depth,
            "status": "success",
            "started_at": "2026-04-25T00:00:00Z",
            "completed_at": "2026-04-25T00:01:00Z",
            "original_direction": delta,
            "files_changed": {
                "added": [{"path": p, "sha256": _sha256(wd / p)}
                          for p in fc["added"]],
                "modified": [{"path": p,
                              "sha256_before": "0" * 64,
                              "sha256_after": _sha256(wd / p)}
                             for p in fc["modified"]],
                "deleted": [],
                "inherited_unchanged": [],
            },
            "what_was_done": delta,
            "delta_vs_parent": delta,
            "metrics": metrics,
            "self_assessment": {
                "succeeded": True,
                "headline": delta,
                "concerns": ["comparative_rigor: no MKL baseline"]
                            if label == "improve" else [],
            },
            "next_steps_hints": ["Test variance"]
                                if label == "validation" else [],
            "build_command": "g++ -O3 main.cpp -o main"
                             if label == "validation" else "",
            "run_command": "./main --out result.csv"
                           if label == "validation" else "",
            "artifacts": [],
            "evaluator_reason": delta,
            "trace_log_summary": "",
            "migration_source": "fresh",
        }
        (wd / "node_report.json").write_text(json.dumps(report, indent=2))

        nodes_dict.append({
            "id": nid,
            "parent_id": parent,
            "depth": depth,
            "label": label,
            "raw_label": "",
            "has_real_data": True,
            "metrics": metrics,
            "eval_summary": delta,
            "artifacts": [],
        })

    (ckpt / "tree.json").write_text(json.dumps({
        "run_id": run_id,
        "experiment_goal": "Optimise SpMM throughput on CPU",
        "nodes": nodes_dict,
    }))

    if with_uploads:
        uploads = ckpt / "uploads"
        uploads.mkdir()
        (uploads / "input_matrix.csv").write_text("row,col,val\n1,1,0.5\n")

    if with_figures:
        (ckpt / "fig_throughput.pdf").write_bytes(b"%PDF-1.4 fake\n")
        (ckpt / "fig_breakdown.png").write_bytes(b"\x89PNG\r\n\x1a\nfake")

    if with_publish_license:
        ear = ckpt / "ear"
        ear.mkdir()
        (ear / "publish.yaml").write_text(
            yaml.safe_dump({"license": with_publish_license}))

    return ckpt


