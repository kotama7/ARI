def test_render_reproduce_sh_falls_back_to_run_job_wrapper(tmp_path: Path):
    """Bug 3b safety net: when node_report's build/run extraction degenerated
    to env-var-only lines (e.g. ``CXX=g++``), `_render_reproduce_sh` should
    fall back to wrapping ``code/run_job.sh`` so the EAR's reproduce.sh
    actually executes something."""
    import importlib
    transform_src = (
        Path(__file__).parent.parent.parent / "ari-skill-transform" / "src"
    ).resolve()
    if str(transform_src) not in sys.path:
        sys.path.insert(0, str(transform_src))
    server = importlib.import_module("server")

    code_dir = tmp_path / "code"
    code_dir.mkdir()
    (code_dir / "run_job.sh").write_text(
        "#!/usr/bin/env bash\n"
        "$CXX $CXXFLAGS spmm.cpp -o spmm\n"
        "./spmm --m 1000\n"
    )
    bad_report = {
        "build_command": "CXX=${CXX:-g++}",
        "run_command": 'CXXFLAGS="-O3 -march=native"',
    }
    body = server._render_reproduce_sh(bad_report, code_dir=code_dir)
    assert body is not None
    assert body.startswith("#!/usr/bin/env bash")
    assert "set -euo pipefail" in body
    # The fallback wrapper should invoke run_job.sh, not embed the env-only
    # build/run lines.
    assert "bash run_job.sh" in body
    assert "fallback wrapper" in body
    # It must NOT contain the bare env-var lines as commands.
    assert "CXX=${CXX:-g++}\n\n\n" not in body  # no orphaned env-only line
    # A1: must promote per-run output artifacts from code/ up to repo root
    # so rubric's expected_artifacts (repo-relative paths) can match them.
    assert "*.csv" in body and "*.pdf" in body
    assert 'cp -f "$_f" "../$_f"' in body


def test_render_reproduce_sh_keeps_substantive_build_run(tmp_path: Path):
    """When node_report's build/run extraction succeeded, prefer the literal
    commands over the wrapper fallback."""
    import importlib
    transform_src = (
        Path(__file__).parent.parent.parent / "ari-skill-transform" / "src"
    ).resolve()
    if str(transform_src) not in sys.path:
        sys.path.insert(0, str(transform_src))
    server = importlib.import_module("server")

    code_dir = tmp_path / "code"
    code_dir.mkdir()
    (code_dir / "run_job.sh").write_text("#!/usr/bin/env bash\necho should-not-be-used\n")
    good_report = {
        "build_command": "g++ -O3 main.cpp -o main",
        "run_command": "./main --out result.csv",
    }
    body = server._render_reproduce_sh(good_report, code_dir=code_dir)
    assert "g++ -O3 main.cpp -o main" in body
    assert "./main --out result.csv" in body
    assert "fallback wrapper" not in body
    assert "bash run_job.sh" not in body


