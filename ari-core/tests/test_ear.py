"""Tests for the Experiment Artifact Repository (issue #4).

The layout is *node_report-driven*:

    <checkpoint>/
    ├── EVOLUTION.md         ← ARI audit log (search trajectory)
    ├── _provenance.json     ← ARI audit log (per-file origin)
    └── ear/
        ├── README.md
        ├── reproduce.sh
        ├── environment.json
        ├── code/                ← verbatim from contributing chain nodes
        ├── data/                ← uploads/ mirror (input only; absent if no uploads)
        ├── figures/             ← top-level *.{pdf,png,svg,jpg,jpeg}
        └── LICENSE              ← optional, SPDX template

EVOLUTION.md and _provenance.json are kept alongside the checkpoint, *not*
inside ear/, so they are never bundled into the published artifact.

Other internal ARI metadata (tree.json, science_data.json, raw_metrics.json,
eval_scores.json, commands.md) also stays at checkpoint root. Experiment
outputs (CSV/log/binary) are not bundled — they are regenerated by
reproduce.sh.

Tests T-C1..T-C18 cover the spec; older tests target the API stability of
the GUI surface (`_api_ear`) and the workflow.yaml stage ordering.
"""
