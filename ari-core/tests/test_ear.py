# ── T-C13: _provenance.json schema ──────────────────────────────────────


def test_TC13_provenance_schema(tmp_path: Path):
    ckpt = _make_chain_checkpoint(tmp_path, with_uploads=True, with_figures=True)
    fn = _get_generate_ear()
    fn(str(ckpt))
    prov = json.loads((ckpt / "_provenance.json").read_text())

    assert prov["schema_version"] == 1
    assert prov["best_node_id"] == "node_validation"
    assert prov["method"] in {"node_report", "fallback_workdir_scan"}
    # files: each has dest, from_node_id, introduced_by, depth_in_chain, size.
    # Paths are checkpoint-relative since _provenance.json now lives at
    # checkpoint root, so files under ear/ are recorded as `ear/...`.
    assert prov["files"]
    for rec in prov["files"]:
        assert rec["dest"].startswith("ear/code/")
        assert rec["from_node_id"]
        assert rec["introduced_by"] in {"added", "modified", "fallback_workdir_scan"}
        assert "depth_in_chain" in rec
        assert "size" in rec
        # No absolute paths and no sha256 in provenance (manifest.lock owns that).
        assert "/tmp" not in rec.get("dest", "")
        assert "sha256" not in rec
    # data records.
    assert any(d["dest"].startswith("ear/data/") for d in prov["data"])
    # figures records.
    assert any(f["dest"].startswith("ear/figures/") for f in prov["figures"])
    # rendered records mention deterministic_render only.
    for r in prov["rendered"]:
        assert r["method"] == "deterministic_render"
    assert "excluded_nodes" in prov
    assert "warnings" in prov


