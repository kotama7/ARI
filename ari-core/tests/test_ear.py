# ── T-C15: idempotent re-run ────────────────────────────────────────────


def test_TC15_generate_ear_is_idempotent(tmp_path: Path):
    ckpt = _make_chain_checkpoint(tmp_path, with_uploads=True, with_figures=True)
    fn = _get_generate_ear()
    fn(str(ckpt))
    snap1 = {p.relative_to(ckpt / "ear"): _sha256(p)
             for p in (ckpt / "ear").rglob("*") if p.is_file()}
    fn(str(ckpt))
    snap2 = {p.relative_to(ckpt / "ear"): _sha256(p)
             for p in (ckpt / "ear").rglob("*") if p.is_file()}
    # All keys should match.
    assert set(snap1.keys()) == set(snap2.keys())
    # All bytes match for "verbatim" files.
    for k in snap1:
        if str(k).startswith("code/") or str(k).startswith("data/") \
                or str(k).startswith("figures/"):
            assert snap1[k] == snap2[k], f"{k} differs across re-runs"


