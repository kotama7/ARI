# ── T-C2: code/ files are byte-equal to their from_node_id work_dir ─────


def test_TC2_code_files_byte_equal_to_origin(tmp_path: Path):
    ckpt = _make_chain_checkpoint(tmp_path)
    fn = _get_generate_ear()
    fn(str(ckpt))
    prov = json.loads((ckpt / "_provenance.json").read_text())
    assert prov["files"], "provenance should record at least one code file"
    workspace = ckpt.parent.parent
    run_id = ckpt.name
    for rec in prov["files"]:
        dest = ckpt / rec["dest"]
        # dest is `ear/code/<rel>`; strip the `ear/code/` prefix to get the
        # work_dir-relative path of the source file.
        rel = Path(rec["dest"]).relative_to("ear/code")
        src = workspace / "experiments" / run_id / rec["from_node_id"] / rel
        assert dest.is_file(), rec
        assert src.is_file(), rec
        assert _sha256(dest) == _sha256(src), (
            f"byte mismatch for {rec['dest']} vs {src}")


