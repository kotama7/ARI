# 4. Memory API: memory.add() not memory.write()
# ══════════════════════════════════════════════

class TestMemoryAPICorrectness:
    def test_memory_client_has_add_not_write(self):
        """MemoryClient must expose add(), not write()."""
        from ari.memory.client import MemoryClient
        assert hasattr(MemoryClient, "add"), "MemoryClient must have add()"
        assert not hasattr(MemoryClient, "write"), "MemoryClient must NOT have write()"

    def test_file_memory_client_add_works(self, tmp_path):
        """FileMemoryClient.add() must persist and be searchable."""
        from ari.memory.file_client import FileMemoryClient
        mc = FileMemoryClient(str(tmp_path / "mem.json"))
        mc.add(
            "EVALUATION_CRITERIA: primary_metric=GFlops_per_s higher_is_better=True",
            metadata={"type": "evaluation_criteria"},
        )
        results = mc.search("GFlops")
        assert len(results) >= 1
        assert "GFlops_per_s" in results[0]["content"]

    def test_loop_uses_memory_add_not_write(self):
        """loop.py must use self.memory.add(), not self.memory.write()."""
        src = Path(__file__).parent.parent / "ari" / "agent" / "loop.py"
        content = src.read_text()
        # Find the EVALUATION_CRITERIA block
        idx = content.find("EVALUATION_CRITERIA:")
        assert idx > 0, "EVALUATION_CRITERIA block must exist"
        block = content[max(0, idx - 200):idx + 200]
        assert "self.memory.add(" in block, \
            "Must use self.memory.add(), not self.memory.write()"
        assert "self.memory.write(" not in block, \
            "self.memory.write() is a bug — MemoryClient has no write() method"


