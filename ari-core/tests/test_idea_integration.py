# 5. Template resolution: _resolve_templates
# ══════════════════════════════════════════════

class TestTemplateResolution:
    def test_idea_context_resolves(self):
        """{{idea_context}} must resolve in _resolve_templates."""
        from ari.pipeline import _resolve_templates

        tpl_vars = {
            "idea_context": "Research direction: RHC Hotset Cache",
            "paper_context": "Full context including idea",
        }
        result = _resolve_templates("Idea: {{idea_context}}", tpl_vars)
        assert result == "Idea: Research direction: RHC Hotset Cache"

    def test_paper_context_includes_idea(self):
        """{{paper_context}} must include idea content when idea.json exists."""
        from ari.pipeline import _resolve_templates

        tpl_vars = {
            "idea_context": "RHC Hotset Cache idea",
            "paper_context": "Static context\n\nRHC Hotset Cache idea\n\nExperiment results",
        }
        result = _resolve_templates("{{paper_context}}", tpl_vars)
        assert "RHC Hotset Cache" in result

    def test_empty_idea_context_resolves_to_empty(self):
        """{{idea_context}} resolves to empty string when no idea.json."""
        from ari.pipeline import _resolve_templates

        tpl_vars = {"idea_context": "", "paper_context": "just results"}
        result = _resolve_templates("prefix-{{idea_context}}-suffix", tpl_vars)
        assert result == "prefix--suffix"

    def test_dot_notation_still_works(self):
        """Dot-notation resolution must not regress."""
        from ari.pipeline import _resolve_templates

        tpl_vars = {
            "stages": {"write_paper": {"outputs": {"file": "/tmp/paper.tex"}}},
        }
        result = _resolve_templates("{{stages.write_paper.outputs.file}}", tpl_vars)
        assert result == "/tmp/paper.tex"


