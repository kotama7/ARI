# 1. BFTS expand: idea_context reaches prompt
# ══════════════════════════════════════════════

class TestBFTSExpandIdeaContext:
    def test_expand_includes_idea_context_in_prompt(self):
        """expand() must include idea_context in the LLM prompt."""
        from ari.config import BFTSConfig
        from ari.llm.client import LLMResponse
        from ari.orchestrator.bfts import BFTS

        mock_llm = MagicMock()
        mock_llm.complete.return_value = LLMResponse(
            content='[{"label": "improve", "direction": "Implement hotset cache"}]'
        )

        bfts = BFTS(BFTSConfig(max_depth=3, max_retries_per_node=2, max_total_nodes=10), mock_llm)
        node = Node(id="n1", parent_id=None, depth=0, has_real_data=True,
                    metrics={"GFlops_per_s": 21.0})

        idea_ctx = "Gap: narrow RHS\nIdea: RHC Hotset Cache\nDescription: selective materialization"
        children = bfts.expand(node, experiment_goal="CSR SpMM benchmark", idea_context=idea_ctx)

        # Verify idea context was included in the LLM prompt
        prompt_sent = mock_llm.complete.call_args[0][0][0].content
        assert "RHC Hotset Cache" in prompt_sent
        assert "selective materialization" in prompt_sent
        assert len(children) == 1

    def test_expand_works_without_idea_context(self):
        """expand() must work with empty idea_context (backward compatible)."""
        from ari.config import BFTSConfig
        from ari.llm.client import LLMResponse
        from ari.orchestrator.bfts import BFTS

        mock_llm = MagicMock()
        mock_llm.complete.return_value = LLMResponse(
            content='[{"label": "improve", "direction": "Tune params"}]'
        )

        bfts = BFTS(BFTSConfig(max_depth=3, max_retries_per_node=2, max_total_nodes=10), mock_llm)
        node = Node(id="n1", parent_id=None, depth=0, has_real_data=True)

        children = bfts.expand(node, experiment_goal="test")  # no idea_context
        assert len(children) == 1

        prompt_sent = mock_llm.complete.call_args[0][0][0].content
        assert "VirSci" not in prompt_sent  # no idea block when empty


